// Programa 01.12: Exemplo de utilização do comando if
package modulo1;

public class ExemploIf {

	public static void main(String[] args) {
		int var1 = 20;
		int var2 = 10;
		
		// modo de uso: if <condição>
		if (var1 > var2) {
			// bloco de comandos do if
			System.out.println("var 1 é maior que var2.");
		}
	}

}
